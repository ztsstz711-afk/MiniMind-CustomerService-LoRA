INSTRUCTIONS = [
    "你是一名电商平台售后客服，请根据用户问题给出礼貌、合规、可执行的回复。",
    "请作为电商售后客服处理用户诉求，说明适用规则、所需信息和下一步操作，不作无法兑现的承诺。",
    "请生成一条专业的电商客服回复：先安抚用户，再核实信息并提供符合平台规则的处理建议。",
]

OPENINGS = [
    "您好，理解您现在的着急和不便。",
    "您好，很抱歉这次情况给您带来了困扰。",
    "您好，您的诉求我已了解，确实会影响购物体验。",
]

TEMPLATES = [
    "{opening}{rule}{info}{next_step}",
    "{opening}为了准确处理，{info_lower}{rule}{next_step}",
    "{opening}{rule}建议您先准备相关信息：{info_short}{next_step}",
]


def build_output(category: str, variant: int) -> str:
    """Build one of three compliant response variants for a category."""
    guidance = GUIDANCE[category]
    info = guidance["info"]
    return TEMPLATES[variant].format(
        opening=random.choice(OPENINGS),
        rule=guidance["rule"],
        info=info,
        info_lower=info.replace("请", "请先", 1),
        info_short=info.removeprefix("请").rstrip("。"),
        next_step=guidance["next"],
    )


def main() -> None:
    random.seed(SEED)
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)

    samples = []
    for category, questions in QUESTIONS.items():
        for question in questions:
            for variant in range(3):
                samples.append(
                    {
                        "instruction": random.choice(INSTRUCTIONS),
                        "input": question,
                        "output": build_output(category, variant),
                        "category": category,
                    }
                )

    with OUTPUT_PATH.open("w", encoding="utf-8") as file:
        for sample in samples:
            file.write(json.dumps(sample, ensure_ascii=False) + "\n")

    print(f"Generated {len(samples)} samples")
    print(f"Output path: {OUTPUT_PATH}")


if __name__ == "__main__":
    main()
